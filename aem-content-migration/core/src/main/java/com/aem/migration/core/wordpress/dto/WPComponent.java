package com.aem.migration.core.wordpress.dto;

public class WPComponent {

	/** The loading. */
	private String loading;

	/** The width. */
	private String width;

	/** The height. */
	private String height;

	/** The src. */
	private String src;

	/** The img src. */
	private String imgSrc;

	/** The alt. */
	private String alt;

	/** The index. */
	private int index;

	/** The img caption. */
	private String imgCaption;


	/** The component type. */
	private String componentType;
	
	/**
	 * Gets the loading.
	 *
	 * @return the loading
	 */
	public String getLoading() {
		return loading;
	}
	
	/**
	 * Sets the loading.
	 *
	 * @param loading the new loading
	 */
	public void setLoading(String loading) {
		this.loading = loading;
	}
	
	/**
	 * Gets the width.
	 *
	 * @return the width
	 */
	public String getWidth() {
		return width;
	}
	
	/**
	 * Sets the width.
	 *
	 * @param width the new width
	 */
	public void setWidth(String width) {
		this.width = width;
	}
	
	/**
	 * Gets the height.
	 *
	 * @return the height
	 */
	public String getHeight() {
		return height;
	}
	
	/**
	 * Sets the height.
	 *
	 * @param height the new height
	 */
	public void setHeight(String height) {
		this.height = height;
	}
	
	/**
	 * Gets the src.
	 *
	 * @return the src
	 */
	public String getSrc() {
		return src;
	}
	
	/**
	 * Sets the src.
	 *
	 * @param src the new src
	 */
	public void setSrc(String src) {
		this.src = src;
	}
	
	/**
	 * Gets the alt.
	 *
	 * @return the alt
	 */
	public String getAlt() {
		return alt;
	}
	
	/**
	 * Sets the alt.
	 *
	 * @param alt the new alt
	 */
	public void setAlt(String alt) {
		this.alt = alt;
	}
	
	/**
	 * Gets the index.
	 *
	 * @return the index
	 */
	public int getIndex() {
		return index;
	}
	
	/**
	 * Sets the index.
	 *
	 * @param index the new index
	 */
	public void setIndex(int index) {
		this.index = index;
	}

	/**
	 * Gets the component type.
	 *
	 * @return the component type
	 */
	public String getComponentType() {
		return componentType;
	}

	/**
	 * Sets the component type.
	 *
	 * @param componentType the new component type
	 */
	public void setComponentType(String componentType) {
		this.componentType = componentType;
	}

	/**
	 * Gets the img src.
	 *
	 * @return the img src
	 */
	public String getImgSrc() {
		return imgSrc;
	}

	/**
	 * Sets the img src.
	 *
	 * @param imgSrc the new img src
	 */
	public void setImgSrc(String imgSrc) {
		this.imgSrc = imgSrc;
	}

	/**
	 * Gets the img caption.
	 *
	 * @return the img caption
	 */
	public String getImgCaption() {
		return imgCaption;
	}

	/**
	 * Sets the img caption.
	 *
	 * @param imgCaption the new img caption
	 */
	public void setImgCaption(String imgCaption) {
		this.imgCaption = imgCaption;
	}

}
